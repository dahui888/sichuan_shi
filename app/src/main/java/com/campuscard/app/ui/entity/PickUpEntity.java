package com.campuscard.app.ui.entity;

public class PickUpEntity {

    private String name;
    private String date;
    private String place;
    private String ecardNo;
    private String ecardStatus;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getEcardNo() {
        return ecardNo;
    }

    public void setEcardNo(String ecardNo) {
        this.ecardNo = ecardNo;
    }

    public String getEcardStatus() {
        return ecardStatus;
    }

    public void setEcardStatus(String ecardStatus) {
        this.ecardStatus = ecardStatus;
    }
}
